package com.cpbpc.pdf.rpg.zh;

import java.util.regex.Matcher;
import java.util.regex.Pattern;

public class RegexExample {
    public static void main(String[] args) {
        String text = "默想：“与喜乐的人要同乐。与哀哭的人要同哭。”（罗12：15）\n" +
                "祷告： 父啊，愿我有同理心和敏感，能把别人的需要放在自己的\n" +
                "需要之上。\n" +
                "爱的担忧\n" +
                "以巴弗提是腓立比教会的领袖之一，也可能是牧师。他被称为“是你\n" +
                "们所差遣的”，他长途跋涉，把为保罗的需要而收集的东西交给保罗(\n" +
                "腓4：18)，并提供教会的最新情况。\n" +
                "我们没有被告知确切的原因，但在前往罗马的途中，他遭受了一场受\n" +
                "伤或疾病，几乎要了他的命。腓立比的基督徒听说了这件事，非常担\n" +
                "心。由于没有像我们这样的现代通讯设备，他们无法知道他是否活了\n" +
                "下来，也不知道他的情况如何。\n" +
                "尽管如此，以巴弗提并没有自怨自艾。他没有为自己的处境感到悲\n" +
                "哀。相反，他非常关心教会在担心他！他“极其难过”，因为他不想让\n" +
                "别人为他担心。的确，他是一个多么无私、关心他人的人。\n" +
                "神的怜悯使以巴弗提得以痊愈。保罗说，这不仅是神对他的怜悯，也\n" +
                "是对保罗的怜悯，因为他若死了，这对保罗来说将是极大的悲哀。保\n" +
                "罗在写这封信的时候，向腓立比人保证，以巴弗提一切安好，并解释\n" +
                "了为什么他认为把他送回他们那里很好。虽然保罗发现以巴弗提很有\n" +
                "用，但他决定“越发急速”(迅速地、殷勤地)打发他往腓立比去，好叫\n" +
                "他们见他好了，就可以喜乐，不必再为他的病情担心。\n" +
                "通过这整件事，我们看到了保罗，以巴弗提，和腓立比教会之间的美\n" +
                "好关系。他们都彼此关心，彼此深爱，他们很容易把别人的需要放在\n" +
                "自己的需要之上。这是一种对彼此真实的、真诚的、无私的关心——\n" +
                "根植于他们对上帝的爱的关心。这是你以前经历过的爱吗?\n" +
                "“…就可以喜乐， \n" +
                "五月二十日，礼拜一\n" +
                "腓立比书二章25节至28节  \n" +
                "罗马书十二章9节至21节 我也可以少些忧愁。”";

        String patternString = "“([^“”]*五月二十日，礼拜一\\s*腓立比书二章25节至28节\\s*罗马书十二章9节至21节[^“”]*)”";
        Pattern pattern = Pattern.compile(patternString);

        Matcher matcher = pattern.matcher(text);

        if (matcher.find()) {
            System.out.println("Match found: " + matcher.group(1));
        } else {
            System.out.println("No match found.");
        }
    }
}

